package Personagens;

public class Guerreiro {
    private String nome;
    private int idade;
    private int ataque;
    private int vida;


    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public void setAtaque(int ataque) {
        this.ataque = ataque;
    }

    public void setVida(int vida) {
        this.vida = vida;
    }

    public String getNome() {
        return nome;
    }

    public int getIdade() {
        return idade;
    }

    public int getAtaque() {
        return ataque;
    }

    public int getVida() {
        return vida;
    }
}
